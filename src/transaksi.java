    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String mobil = jComboBox1.getSelectedItem().toString();
            idMobil = Integer.parseInt(mobil.split(" - ")[0]);

            Connection c = Koneksi.getConnection();
            PreparedStatement ps = c.prepareStatement(
                    "SELECT harga_sewa FROM mobil WHERE id_mobil=?");
            ps.setInt(1, idMobil);
            ResultSet rs = ps.executeQuery();

            if (rs.next()) {
                hargaSewa = rs.getDouble("harga_sewa");
            }

            LocalDate tglSewa = LocalDate.parse(jTextField2.getText());
            LocalDate tglKembali = LocalDate.parse(jTextField3.getText());

            long lama = ChronoUnit.DAYS.between(tglSewa, tglKembali);
            double total = lama * hargaSewa;

            jTextField4.setText(String.valueOf(total));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Format tanggal: yyyy-mm-dd");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Connection c = Koneksi.getConnection();

            // cari id pelanggan
            PreparedStatement psP = c.prepareStatement(
                    "SELECT id_pelanggan FROM pelanggan WHERE nama=?");
            psP.setString(1, jTextField1.getText());
            ResultSet rsP = psP.executeQuery();

            if (!rsP.next()) {
                JOptionPane.showMessageDialog(this, "Pelanggan tidak ditemukan");
                return;
            }
            idPelanggan = rsP.getInt(1);

            // simpan transaksi
            String sql = "INSERT INTO transaksi_sewa VALUES (NULL,?,?,?,?,?)";
            PreparedStatement ps = c.prepareStatement(sql);
            ps.setInt(1, idMobil);
            ps.setInt(2, idPelanggan);
            ps.setString(3, jTextField2.getText());
            ps.setString(4, jTextField3.getText());
            ps.setDouble(5, Double.parseDouble(jTextField4.getText()));
            ps.executeUpdate();

            // update status mobil
            PreparedStatement ps2 = c.prepareStatement(
                    "UPDATE mobil SET status='Disewa' WHERE id_mobil=?");
            ps2.setInt(1, idMobil);
            ps2.executeUpdate();

            JOptionPane.showMessageDialog(this, "Transaksi berhasil disimpan");
            loadTransaksi();
            loadMobil();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

