    private void loadMobil() {
        try {
            Connection c = Koneksi.getConnection();
            String sql = "SELECT * FROM mobil WHERE status='Tersedia'";
            PreparedStatement ps = c.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            jComboBox1.removeAllItems();
            while (rs.next()) {
                jComboBox1.addItem(
                        rs.getInt("id_mobil") + " - " + rs.getString("merk")
                );
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    private void loadTransaksi() {
        model.setRowCount(0);
        try {
            Connection c = Koneksi.getConnection();
            String sql = """
                SELECT t.id_transaksi, m.merk, p.nama,
                       t.tanggal_sewa, t.tanggal_kembali, t.total_bayar
                FROM transaksi_sewa t
                JOIN mobil m ON t.id_mobil=m.id_mobil
                JOIN pelanggan p ON t.id_pelanggan=p.id_pelanggan
                """;
            PreparedStatement ps = c.prepareStatement(sql);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt(1),
                    rs.getString(2),
                    rs.getString(3),
                    rs.getDate(4),
                    rs.getDate(5),
                    formatRupiah(rs.getDouble(6))

                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }
    
    private String formatRupiah(double angka) {
    Locale indo = new Locale("id", "ID");
    NumberFormat rupiah = NumberFormat.getCurrencyInstance(indo);
    return rupiah.format(angka);
}

