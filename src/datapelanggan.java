    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection c = Koneksi.getConnection();
            String sql = "INSERT INTO pelanggan VALUES (NULL,?,?,?,?)";
            PreparedStatement ps = c.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());
            ps.setString(3, jTextField3.getText());
            ps.setString(4, jTextArea1.getText());
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data pelanggan berhasil disimpan");
            loadData();
            resetForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Connection c = Koneksi.getConnection();
            String sql = "UPDATE pelanggan SET nama=?, no_ktp=?, no_hp=?, alamat=? WHERE id_pelanggan=?";
            PreparedStatement ps = c.prepareStatement(sql);
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());
            ps.setString(3, jTextField3.getText());
            ps.setString(4, jTextArea1.getText());
            ps.setInt(5, idPelanggan);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data pelanggan berhasil diubah");
            loadData();
            resetForm();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Hapus data pelanggan ini?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi == JOptionPane.YES_OPTION) {
            try {
                Connection c = Koneksi.getConnection();
                String sql = "DELETE FROM pelanggan WHERE id_pelanggan=?";
                PreparedStatement ps = c.prepareStatement(sql);
                ps.setInt(1, idPelanggan);
                ps.executeUpdate();

                JOptionPane.showMessageDialog(this, "Data pelanggan berhasil dihapus");
                loadData();
                resetForm();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

