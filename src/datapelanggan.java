    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
       int row = jTable1.getSelectedRow();
    
    // pengaman jika tidak ada baris yang dipilih
    if (row < 0) {
        return;
    }

    // ambil ID pelanggan
    idPelanggan = Integer.parseInt(model.getValueAt(row, 0).toString());

    // isi field dari tabel
    jTextField1.setText(model.getValueAt(row, 1).toString()); // nama
    jTextField2.setText(model.getValueAt(row, 2).toString()); // no ktp
    jTextField3.setText(model.getValueAt(row, 3).toString()); // no hp
    jTextArea1.setText(model.getValueAt(row, 4).toString());  // alamat 
    }//GEN-LAST:event_jTable1MouseClicked

